# Задача 1 — Переменные и типы + форматирование
# Суть:
# У тебя есть скорость в км/ч, нужно посчитать скорость в м/с и красиво вывести.
# Что сделать:
# Создай переменную speed_kmh с любым целым значением (например, 90).
# Преобразуй её в метры в секунду по формуле: м/с = км/ч ÷ 3.6. Сохрани в speed_ms.
# Сформируй строку вида:
# Скорость: X км/ч (int) -> Y м/с (float, округлено до 2 знаков)
# Выведи:
# значение и тип speed_kmh,
# значение и тип speed_ms до округления,
# итоговую строку с округлением до 2 знаков (используй форматирование строки).
# Подсказки (без кода):
# Деление на 3.6 даст float.
# Для округления до 2 знаков подойдёт форматирование f-строкой с спецификатором.
# Тип можно получить стандартной функцией.
# Обрати внимание на разницу между самим значением speed_ms и тем, как ты его отображаешь (округляешь только в выводе).
# Проверки для себя:
# При speed_kmh = 36 должно получиться ровно 10.0 м/с (потому что 36 / 3.6 = 10).
# Тип speed_kmh — int, тип speed_ms — float.
# В итоговой строке м/с отображаются с двумя знаками после запятой.
# Сделаешь — кидай сюда результат (вывод и свой код, если хочешь проверить), и поедем к следующей задаче.

speed_kmh = 90
speed_ms = speed_kmh / 3.6
print(f"Скорость {speed_kmh} км/ч -> {float(speed_ms):.2f} м/с \n{type(speed_kmh)} {type(speed_ms)}")


# Задача 2 — Строки
# Дана строка:
# text = "Hello, World!"
# Нужно:
# Сделать её полностью в верхнем регистре.
# Проверить, начинается ли строка с "Hello".
# Вывести длину строки.

text = "Hello, World!"
# переводим в верхний регистр
upper = text.upper()
